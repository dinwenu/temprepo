    # > torch.Size([32, 128])
    # > torch.Size([32, 128])
    # > torch.Size([32, 128])
    # > torch.Size([32])
    for t in minibatch:
        if t.shape[0] != defined_minibatch_size: # last minibatch can be fractional
            if verbose:
                print("[INFO] minibatch's tensor is not defined size: {}".format(t.shape))
